/* ── Zustand Store ── */
const dictionaries: Record<Locale, Record<string, string>> = { en, es, pt }

interface I18nState {
  locale: Locale
  setLocale: (locale: Locale) => void
}

export const useI18nStore = create<I18nState>((set) => ({
  locale: 'en',
  setLocale: (locale) => set({ locale }),
}))

/** Reactive hook — components re-render when locale changes */
export function useTranslation() {
  const locale = useI18nStore((s) => s.locale)
  const t = (key: string): string => {
    return dictionaries[locale]?.[key] ?? dictionaries.en[key] ?? key
  }
  return { t, locale }
}

/* ── Legacy compat — keep old API working ── */
export function setLocale(locale: Locale): void {
  useI18nStore.getState().setLocale(locale)
}

export function getLocale(): Locale {
  return useI18nStore.getState().locale
}

/** Translate a key. Returns string for current locale with English fallback. */
export function t(key: string): string {
  const { locale } = useI18nStore.getState()
  return dictionaries[locale]?.[key] ?? dictionaries.en[key] ?? key
}
